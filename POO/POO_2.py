'''
Vocabulario de la POO

Clase: 
Modelo donde se redactan las características comundes de un grupo de objetos.

Instancia:
Es el objeto o ejemplar perteneciente a una clase.

Modularización:
Proceso por el cual seleccionamos y agrupamos instrucciones de programación que cumplen una función específica
Ventajas:
	- Cada uno de los modulos funcionan de forma independiente.
	- Permite reutilizar trozos de codigo de un programa en otro.
	- El progrma no cae, si falla alguna linea de codigo.

Encapsulación: 
El funcionamiento de una clase de un programa puede estar protegido de otras clases
Todas las clases de alguna forma estan conectadas entre si para que puedan funcionar como equipo pero a la ves cada clase esta encapsulada.
Para ellos las clases se conectan con los "métodos de acceso".



'''