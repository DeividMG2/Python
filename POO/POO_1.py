'''
POO: Programación Orientada a Objetos.
Hay 2 categorías de los lenguajes, los de programacion orientada a procedimientos que son los lenguajes de programación antiguas(60s y 70s) y la programacion orientada a objetos son los lenguajes de programación modernos.

Programación Orientada a Procedimientos:

Ejemplos de lenguajes: Fortran, Cobol, Basic, etc.
Desventajas:
	- Muchisimas lineas de codigos
	- Codigos complejos de descifrar 
	- Son poco reutilizables.
	- Es muy probable que el programa caiga, si hay algun fallo en alguna linea de codigo.
	- Aparición frecuente de codigo espagueti. O sea son saltos de codigo adelantes y atras
	- Muy dificil de depurar por otros programadores en caso de error o necesidad.

Programación Orientada a Objetos:

Consiste en trasladar la naturaleza de los objetos de la vida real al código de programación.
Los objetos tienen un estado, un comportamientos(Que puede hacer?), y unas propiedades
Ejemplo:
Un Objeto Coche:
¿Cuál es el estado de un coche?Un coche puede estar parado, circulando, aparcado, etc..
¿Qué propiedades tiene un coche? Un coche tiene  un color, un peso, un tamaño, etc.
¿Qué comportamientos tiene un coche? Puede arrancar, frenar, acelerar, girar, etc.

Algunos ejemplos de lenguajes que lo usan: C++,Java, Visual.Net, etc
Ventajas:
	- Programas dividios en "trozos", "partes","modulos" o "clases",
	es decir, Modularización
	- Muy reutilizable. Herencia.
	- Si existe fallo en alguna linea del codigo, el programa continuará con su funcionamiento. Excepciones.
	- Encapsulamiento.

'''
